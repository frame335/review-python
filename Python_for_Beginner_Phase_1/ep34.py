### Assignment 9: ป้อนตัวเลขหาผลรวมตัวเลข

##################################################################################
start , stop = 1,5 # กำหนดเป็นค่ำจำนวนที่จะทำซ้ำ
sum, avg = 0,0

while (start<=stop):
    number = int(input("ป้อนตัวเลขของคุณ : "))
    sum+=number # บวกเลขที่ป้อนไปในแต่ละรอบ
    start+=1

avg = sum / stop
print("ผลรวม = ",sum)
print("ค่าเฉลี่ย = ",avg)


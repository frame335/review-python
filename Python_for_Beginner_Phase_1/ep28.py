## แสดง helloworld ออกมาสามรอบ

i = 1 # เป็นตัวนับจำนวนรอบ เป็นค่าเริ่มต้น

while i<=5: # 1<=3, 2<=3, 3<=3, 4<=3 เป็น false
    print("Hello World รอบที่ ",i)
    i=i+1 # พอ i เป็น 4 เป็นเท็จมันก็จะออกจาก loop
    print("จบรอบที่ ",i-1)

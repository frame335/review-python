### Break / Continue # ทำงานร่วมกับ loop

##################################################################################
# break ใช้ในการหยุดการทำซ้ำ

i = 1

while i<=10: #รอบที่ 4 , 5 
    print("รอบที่ = ",i)
    if (i==5):
        break # มันจะเบรครอบที่ 5
    i+=1 
# else:

print("จบโปรแกรม")


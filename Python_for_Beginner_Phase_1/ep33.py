# contimue ใช้ในการกระโดดข้าม , ต้องเพิ่มค่าก่อนค่อยทำการเช็คค่า และเซ็ตค่าเริ่มต้นให้เป็น 0
j = 0

while j< 10: #รอบที่ 4 , 5  # ต้องแก้ตั้งแต่ตรงนี้
    # ถ้า i มันเป็นรอบที่ 10 รอบที่ 11 มันจะติดมาทำไงดีวะ
    # มันก็จะเกินมา รอบที่ 11 มันเกิดจากการบวกเพิ่มก่อนที่เราจะแสดงผลไงล่ะ
    j+=1 #4 ไปเช็ค พอเป็น 5 มันจะกระโดดข้ามไป
    if (j==5):
        continue # มันจะเบรครอบที่ 5
    print("รอบที่ = ",j)
# else:

print("จบโปรแกรม")


# ถ้าเป็นเลขคี่ให้มันกระโดดข้าม
k = 0

while k<=10: #รอบที่ 4 , 5 
    # ถ้า i มันเป็นรอบที่ 10 รอบที่ 11 มันจะติดมาทำไงดีวะ
    # มันก็จะเกินมา รอบที่ 11 มันเกิดจากการบวกเพิ่มก่อนที่เราจะแสดงผลไงล่ะ
    k+=1 #4 ไปเช็ค พอเป็น 5 มันจะกระโดดข้ามไป
    if (k%2 != 0):
        continue # มันจะเบรครอบที่ 5
    print("รอบที่ = ",k)

print("จบโปรแกรม")



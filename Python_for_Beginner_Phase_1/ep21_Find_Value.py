### หาเลขคู่ / เลขคี่ืี

number = int(input("ป้อนตัวเลขของคุณ :"))

# ถ้าหาร 2 ลงตัว => คู่
if number % 2 == 0:
    print("เป็นเลขคู่")
else:
    print("เป็นเลขคี่")

